package com.murtaza.staticExample;

public class Department {
    // instance variables
    int dept_id;
    String dept_name;
    int dept_ranking;
    // Parameterised constructor
    Department(int dept_id, String dept_name, int dept_ranking){
        this.dept_id = dept_id;
        this.dept_name = dept_name;
        this.dept_ranking = dept_ranking;
    }
}
